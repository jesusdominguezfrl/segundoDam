    private class gestorCandados implements JCandadoListener{

        @Override
        public void bloqueado(JCandadoEvent evt) {
            bloqueados.addElement(evt.getSource());
            consistencia();
        }

        @Override
        public void seleccionado(JCandadoEvent evt) {
            seleccionados.addElement(evt.getSource());
            consistencia();
        }
        
    }
    
    private void consistencia(){
        jPanelCandados.repaint();
        jPanelCandados.revalidate();
        jButtonCierraTodos.setEnabled(seleccionados.size()>0);
        jButtonDesbloquear.setEnabled(jListCandadosBloqueados.getSelectedIndex()!=-1);
        jButtonCrearNuevosCandados.setEnabled("".equals(jTextFieldNumeroCandadosNuevos.getText()));
    }
    
