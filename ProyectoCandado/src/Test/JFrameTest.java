    private void jButtonCierraTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCierraTodosActionPerformed
        for (int i = 0; i < seleccionados.size(); i++) {
            ((JCandado)seleccionados.get(i)).cerrar();
            seleccionados.remove(i);
        }
        consistencia();
    }//GEN-LAST:event_jButtonCierraTodosActionPerformed

    private void jButtonDesbloquearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDesbloquearActionPerformed
        
        for (Object o : jListCandadosBloqueados.getSelectedValues()) {
            ((JCandado)o).desbloquear();
            bloqueados.removeElement(o);
        }
        consistencia();
    }//GEN-LAST:event_jButtonDesbloquearActionPerformed

